package com.kassmon.modules.console.api;

/**
 * The OutputLevel enum represents different levels of console output.
 * Each level corresponds to a different visual representation in the console.
 */
public enum OutputLevel {
	/** Default output level with standard formatting */
	DEFAULT,
	/** Informational messages */
	INFO,
	/** Error messages */
	ERROR,
	/** Warning messages */
	WARNING,
	/** Success messages */
	SUCCESS
}
